           'product.required' => 'القسم مطلوب',
           'Amount_collection.required' => 'مبلغ التحصيل مطلوب',
           'Amount_collection.max' => 'مبلغ التحصيل غير صحيح',
           'Amount_Commission.required' => 'مبلغ التحصيل مطلوب',
           'Amount_Commission.max' => 'مبلغ التحصيل غير صحيح',

       ];
     }

     protected function getRules()
      {
         return $rules =[

             'invoice_number' => 'required|unique:invoices|max:50 ',
             'Due_date' => 'required',
             'product' => 'required',
             'Amount_collection' => 'required|max:8 ',
             'Amount_Commission' => 'required|max:8 ',

         ];
    }
